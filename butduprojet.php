                        <h3>Problématique </h3>
                        <p> 
                            Notre client, M. Philippe Guillem a souhaité que l’on  apporte des réponses à la question suivante : <u>Quels sont les impacts de la robotique éducative sur la persévérance scolaire dans le parcours de l’élève ?</u>
                        </p>
                    </div>
                </div>
            </div>
            <div class="col-md-12 margin_bottom">
                <div class="row">
                    <div class="Games">
                        <h3>Antécédents </h3>
                        <p> 
                            Le travail des étudiants de l’année précédente qui ont été les premiers à se lancer sur le projet leur a permis de dresser les premières conclusions. Leurs observations dans les classes élémentaires de CE1 et de CM2 et dans une classe de maternelle ainsi que les notes des élèves obtenues grâce à leur site web ont montré que les <strong>activités de robotique éducative</strong> étaient très <strong>appréciées</strong> de tous les élèves et que dans l’ensemble elles n’ont <strong>pas</strong> été perçues comme <strong>insurmontables</strong> par les élèves. La difficulté rencontrée est la plus élevée pour les activités de type défi. Enfin, ils ont montré qu’il n’existe pas de corrélation linéaire entre la difficulté et le plaisir. Ainsi, le fait que les élèves apprécient une activité ne dépend pas de la difficulté de celle-ci. Utiliser des robots permet donc de rendre la difficulté moins rebutante. 
                        </p>        
                        <p>
                            Ces conclusions fortes intéressantes sont cependant basées sur une <strong>faible quantité de données</strong> ce qui amenuise leur qualité. Ainsi, nous avons jugé qu’il était important de pallier cela en concentrant nos efforts sur un recueil massif de données. 
                        </p>        
                        <p>
                            Leur travail nous a permis de cerner plus rapidement l’enjeu du nôtre, mais le <strong>contexte sanitaire</strong> ayant eu un impact sur sa progression, cela nous a forcé à reprendre certains points comme le site web et les grilles d’observation. Par ailleurs, étant donné que nous avons observé des classes et donc des élèves différents de l’année dernière (uniquement des classes maternelles), nous n’avons pas pu assurer un suivi pédagogique des élèves et donc une analyse temporelle des résultats. 
                        </p>
