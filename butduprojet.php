                        <h3>Description</h3>
                        <p> 
                            Notre projet transdisciplinaire a été lancé en <strong>octobre 2019</strong> par l’Atelier Canopée 33 de Mérignac et amorcé par un premier groupe d’étudiants de l’ENSC pendant une année. L’objet de ce projet est de construire une <u>progression pédagogique en robotique éducative de la maternelle au CM2</u> au sein de l’école maternelle et élémentaire Gambetta à Talence qui participe à un projet expérimental de l’Atelier Canopé.
                        </p>        
                        <p>
                            Les élèves travaillent avec des robots éducatifs, expérimentent, résolvent des situations problèmes mettant en jeu des compétences de l’ordre de la pensée computationnelle et des algorithmes. Le projet sur trois ans doit permettre de dégager et proposer une progression pédagogique de la maternelle au collège.
                        </p>
                    </div>
                </div>
