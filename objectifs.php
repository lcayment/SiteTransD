                                Les objectifs du projet ont été posés l’année dernière, bien qu’avec la crise sanitaire, ceux-ci n'ont pas été entièrement atteints.<strong> L’objectif principal était de mettre en évidence l’impact de l’utilisation de robots, en milieu scolaire, sur la persévérance scolaire.</strong> Il s’agissait, cette année, de reprendre plusieurs points. Nous avions donc pour objectifs de base de faire des <strong>séances d’observation sur les maternelles </strong>suivants le programme Canopé avec Bluebot. Il fallait d’abord tester les grilles d’observation et les adapter à nos propres observations afin d’en tirer un maximum d’informations. Un autre des objectifs que nous souhaitions atteindre était de <strong>programmer une application </strong> qui permettrait de récolter les données concernant les ressentis des enfants, directement dans une base de données, accessible à tout moment par tous les membres de l’équipe. Ensuite, à mi-parcours, nous avons mis à jour ces objectifs. L’organisation de <strong>focus group dans des classes de primaires accompagné de petites interviews faites aux professeurs nous a paru essentiel pour remarquer les effets de l’utilisation de robots sur le long terme. </strong> De plus, suite à une réunion avec notre client, Philippe Guillem, et l’institutrice de maternelle, Cannelle Parodi, nous avons posé d’autres objectifs. Bien que les effets sur le long terme soient importants, il nous a paru judicieux de regarder l’impact à la maison, pour <strong>voir le réel intérêt que portent les enfants sur ces activités par le biais d’un questionnaire</strong> envoyé aux parents. Enfin, nous devions <strong>élaborer un livret de compétence </strong> pour assurer le suivi de l’enfant tout au long de l’année.
