                <div class="col-md-12 margin_bottom">
                    <div class="row">
                        <div class="Games">
                            <h3>Le plaisir</h3>
                            <p> 
                                Nous avons observé qu’avant chaque séance de robotique, les élèves ressentaient de <strong>l’excitation</strong>, de l’<strong>impatience</strong> et manifestaient <strong>beaucoup d’intérêt</strong> pour ce qu’ils apprenaient et manipulaient, en l'occurrence, le robot Bluebot. Nous avons noté que cet <strong>enthousiasme</strong> et <strong>curiosité</strong> perduraient durant l’intégralité de la séance et ce, indépendamment de la nature de la séance (découverte ou résolution de problème sous forme de défis). Nous avons aussi noté tout le côté ludique que suscite la manipulation du robot Bluebot. Les élèves conçoivent davantage les activités de robotique comme un jeu les mettant au défi de réussir. Ces séances les obligent à prendre en main le robot et à  manipuler ce qui les rendent actifs et leurs permettent d’appliquer les concepts appris antérieurement. Nous pouvons alors en tirer une première conclusion : <u>solliciter activement l’enfant dans une démarche ludique consistant à leurs apprendre et à les faire appliquer leurs connaissances par la manipulation du robot est une méthode éducative qui suscite chez eux beaucoup de plaisir. </u>
                            </p>

                            <p> 
                                Lors des séances, les élèves sont séparés en <strong>groupe</strong>. Elles s’articulent le plus souvent sous forme de phases consignes/manipulations qui s’enchaînent. Lors de la phase de consignes, la maîtresse revient sur des notions déjà vues ou en cite des nouvelles et demandent aux groupes d’enfants d’accomplir des tâches de découverte ou de défis. Celles-ci sont mises en action lors de la phase de manipulation. Ainsi ces séances reposent sur un travail collectif. Nous avons observé que le travail en équipe incitaient les élèves à s’impliquer dans l’activité. L’enfant est désireux de donner son avis et d’essayer des actions pour atteindre l’objectif final. Il veut montrer ce dont il est capable. Nos dernières séances d’observations nous ont montrées que les élèves, ayant acquis une certaine autonomie à l’égard de la manipulation de Bluebot, une fois lancés dans la résolution d’un problème, persistent et échangent entre eux pour trouver des solutions. Ils ont compris l’enjeu du travail de groupe qui leur permet de mettre en commun leurs connaissances et leurs idées dans l’espoir de trouver une solution à leur problème et de parvenir à la résolution du défi. </br>
                                Ainsi, <u>le travail de groupe les stimule et les pousse à persévérer dans la résolution du problème.</u>
                            </p>

                            <p> 
                                Nous avons aussi remarqué l’importance de la <strong>reconnaissance de la maîtresse</strong> vis-à-vis de l’élève. Ces derniers la réclament continuellement, dès qu’ils terminent une tâche demandée. Cette reconnaissance leur permet d’augmenter leur confiance et leur estime d’eux. Cela est un moteur à leur motivation et à leur engagement dans l'activité. Ils prennent plaisir à continuer et persévèrent dans leur raisonnement. L’encouragement et l’implication de l’éducatrice est donc essentielle à la persévérance. 
                            </p>    

                            <p>
                                Ainsi, nous pouvons en conclure que <strong>l’aspect ludique, le travail en groupe et la reconnaissance de la maîtresse</strong> sont des facteurs clés influençant le plaisir ressenti par l’élève lors de la séance. 
                            </p>
                        </div>
