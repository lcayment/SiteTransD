                        <div class="Games">
                            <h3>La difficulté </h3>
                            <p> 
                                Nous avons observé que les élèves qui avaient mal compris la consigne ou les nouvelles notions abordées ont naturellement plus de difficultés à réaliser la tâche demandée. Lors des séances de découverte (les premières séances de robotique), les élèves doivent à la fois s’accaparer de nouveaux concepts et les mettre en application. Cela représente une difficulté pour certains.  Cela semble évident mais il est important de le rappeler. Il est important de bien dissocier l’aspect théorique de l’aspect expérimental. C’est pourquoi <strong>des cours en amont ou la verbalisation à haute voix des instructions</strong> sont essentiels. Un élève qui comprend parfaitement les consignes et comprend les concepts qu’il manipule aura plus de facilité à réaliser les tâches.
                            </p>

                            <p> 
                                Nous avons aussi remarqué que la <strong>façon d’appréhender un problème</strong> impacte fortement sa résolution. Les élèves qui travaillent <u>étape par étape</u> de façon linéaire ont beaucoup plus de facilité à résoudre le problème comparé à ceux qui l’imaginent de façon globale. <u>Séquencer le problème</u> en plusieurs parties leur permet de progresser petit à petit et de visualiser concrètement leur avancée. Il s’agit là d’un gage de persévérance. <br>
                                Par ailleurs, là aussi le travail en groupe à son rôle à jouer. En effet, il favorise l’entraide entre les élèves et les aident à affronter la difficulté à plusieurs. 
                            </p>

                            <p> 
                                Finalement, <strong>la compréhension des consignes et des notions, la façon d’appréhender un problème et le travail de groupe</strong> sont les éléments que nous avons relevés qui influencent sur la difficulté ressentie par l’élève à résoudre un problème.
                            </p>

                            <p>
                                Pour conclure, nos observations nous ont permis d’aboutir à dresser la liste de quelques facteurs influençant la persévérance de l’élève dans la résolution de problèmes. Il serait désormais intéressant de voir si ses stratégies de résolution sont aussi mises à profit dans des activités plus scolaires.
                            </p>
