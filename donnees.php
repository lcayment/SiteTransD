                            <p>
                                Ces données ont été récoltés <u>via les observations</u> concentrées dans un journal de bord ainsi que grâce aux <u>grilles d'observations</u>.
                            </p>
                            <p>
                                L’analyse que nous allons porter ici est entièrement basée sur des données récoltées grâce à nos observations lors des séances dédiées à la robotique. Etant donné que nous ne sommes pas arrivées au terme de l’application d’auto-évaluation, nous ne bénéficions <u>pas de données quantifiables</u>. 
                            </p>
                            <p> 
                                <strong>Pourquoi l'observation ? </strong> <br> L’observation est, dans le cadre de notre projet, une méthode très pertinente pour la récolte de données car elle nous permet de recueillir des informations objectives et diverses concernant à la fois <u>l’ambiance générale, le comportement des élèves avec le robot, entre eux et avec la maîtresse</u>. Cependant, <u>l’absence de données quantitatives</u> et le <u>faible nombre d’observations</u> rendent la qualité de notre analyse moins rigoureuse.
                            </p>
                            <p> 
                                Les observations que nous avons menées nous ont permis de dresser différents éléments influençant le plaisir et la difficulté ressenties par les élèves et donc implicitement leur persévérance au sein d’une activité.
                            </p>
                        </div>
