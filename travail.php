                            <h3>Les focus group </h3>
                            <p> Les focus group faisaient partie des objectifs du deuxième semestre. Nous n’avons malheureusement pas pu les réaliser en raison de la situation sanitaire. Cependant nous avons préparé le déroulement d’une séance. </p>

                            <p>Le but premier de ces séances est de collecter des informations sur le ressenti des enfants directement avec eux. Ils ont alors la possibilité de s’exprimer vis-à-vis de leur expérience avec la robotique. </p>

                            <p> Les séances se déroulent en demi-classe. Cela permet de sonder un groupe ni trop grand ni trop petit. En effet, si les enfants sont tous ensemble, un phénomène d’influence sera d’autant plus présent. En effet, les enfants auraient peut être tendance à suivre leurs copains.ines. A noter que ce phénomène persistera également en demi-classe mais il sera, selon nous, moins marqué. L’avantage de ces demi-groupes est double car il permet aussi aux professeurs de travailler avec l’autre groupe sur une autre activité, peut être non réalisable en classe entière. </p>

                            <p>Quant à la séance en elle-même, elle se découpe en 4 étapes : </br>
                            - <u>La présentation :</u> cette étape ne dure pas très longtemps car elle consiste juste à ce que nous nous présentions pour expliquer aux enfants pourquoi nous sommes là avec eux. </br>
                            - <u>Le jeu de présentation :</u> afin que les enfants soient un peu plus en confiance et à l’aise avec nous, nous voulions réaliser un jeu de présentation pour que nous apprenions à les connaître, du moins à connaître leur prénom. Ce jeu peut se présenter sous forme de différents jeux (Lucky Luke des prénoms, cercle des prénoms, etc). L’avantage pour nous de connaître les noms est le fait que nous pourrons solliciter tous les enfants en les appelant, même ceux qui semblent être les plus timides.</br>
                            - <u>La présentation du questionnaire :</u>le cœur de la séance est le questionnaire. Nous voulons cependant le présenter comme un jeu en insistant sur le fait qu’il n’y a pas de mauvaise réponse et que tout le monde peut s’exprimer.</br>
                            - <u>Le questionnaire :</u> cette étape est la plus longue et dure environ 15 minutes. Ici on pose aux enfants des questions sur les séances de robotique et, selon leur réponse, il se déplace d’un côté ou de l’autre de la classe. Il est également possible de rester au milieu lorsqu’ils ne savent pas ou qu’ils ne veulent pas répondre. Chaque question est suivie d’une discussion avec les enfants pour qu’ils puissent préciser leur réponse.</br>
                            </p>

                            <p>Ces séances devraient durer 30 à 45 min environ de la présentation à la fin du questionnaire.</p>
