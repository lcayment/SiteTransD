                            <h3>Les observations</h3>
                            <p> Ces nouvelles grilles d’observation ont été testées dans les classes durant les mois de novembre et décembre. En effet, nous avons eu la possibilité de réaliser sept observations entre le 12 novembre 2020 et le 3 décembre 2020. Le niveau de la classe suivie était un mélange de moyenne section et de grande section. Les enfants avaient donc environ 4 à 6 ans. Cette différence d’âge et donc de compétences s’est fait ressentir durant les séances mais l’avantage de mélanger les niveaux est l’entraide et le partage des savoirs entre les élèves. </p>

                            <p> Les séances durent environ 1h30 et se déroulent dans la salle de motricité de l’école maternelle Gambetta. Cette salle assez spacieuse permet aux enfants d’avoir de l’espace et de sortir de leur salle de classe habituelle. Une séance se déroule globalement de la même manière avec une alternance d’interactions avec la maîtresse qui pose des questions pour stimuler les élèves et des séances en groupe. Durant ces séances en groupe les enfants sont répartis par groupe de 3-4 élèves avec une problématique à résoudre. Ils peuvent réagir et discuter de la meilleure stratégie pour résoudre le problème. </p>

                            <p>Après plusieurs séances il a été remarqué que selon l’état d’esprit des élèves, les séances peuvent durer plus ou moins longtemps. En effet, si la séance est trop difficile ou que les enfants sont fatigués par exemple, ces derniers auront tendance à perdre leur attention et à ne plus rien produire. Il est donc important de rester flexible et observateur quant au déroulement de la séance. </p>

                            <p>Ces observations ont permis dans un premier temps d’améliorer nos grilles initiales mais également d’appréhender une séance de robotique à l’école. Elles auraient dû être complétées par des séances de focus group décrites dans la partie suivante ainsi que par une auto-évaluation des élèves via une application décrite également plus bas.</p>

                            </p>
