                            <h3>L'application mobile </h3>
                            <p>L’application mobile, demandée par le client, doit permettre aux enfants de s’auto-évaluer sur deux critères : le plaisir et la difficulté. Ces deux critères ont été choisis parmi ceux intervenant dans la persévérance scolaire. Cette auto-évaluation permet d’être en contact direct avec les enfants et leur expérience de la séance. </br> 
                            L’idée était donc de développer une application sur mobile ou tablette que les enfants pourraient utiliser seul. </p>

                            <p>Dans un premier temps nous avons donc réalisé des recherches sur le format et la forme que l’application allait prendre. Ces recherches sont regroupées dans une section à retrouver ici (ajouter le lien de la page des recherches). </p>

                            <p>Une fois ces recherches complètes, une maquette a été réalisée afin de nous aider à avoir un visuel sur l'application. Nous avons pu présenter cette maquette au client et au corps enseignant de l’école Gambetta pour validation. Ci-dessous vous retrouverez les maquettes :</p>
                            <figure><img src="images/maquettes.png" alt="#"></figure>

                            <p>La première page permet à l’enfant de choisir un avatar qui va le suivre tout au long de l’auto-évaluation. On identifie ensuite l’état d’esprit dans lequel l’enfant se trouve. En effet, son comportement vis-à-vis de la séance peut énormément varier en fonction de son émotion initiale. C’est pour cela qu’on accueillera les émotions en début et en fin de séance pour potentiellement aussi constater une amélioration ou une dégradation. </br>
                            Les deux autres pages sont les pages pour identifier la difficulté ressentie par l’enfant. Cette difficulté est imagée par une montagne. Enfin le plaisir est relié à la météo, si l’enfant a apprécié l’activité, alors il peut rapprocher le curseur du soleil, sinon des nuages. </br>
                            Il était important d’imager au maximum l’application pour que les enfants puissent exprimer leurs émotions et ressentis de manière pertinente.
                            </p>

                            <p>Ces maquettes pourraient être améliorées. En effet, les enfants de maternelle ne peuvent pas l’utiliser seuls car ils ne savent pas encore lire.  </br>
                            La maquette étant validée, nous avons pu commencer à la développer. Cependant, par manque de temps, nous n’avons pas pu réaliser toute l’application mais seulement le côté front-end. En effet le back-end doit se composer d’une partie récupérant les données d'auto-évaluation dans une base de données. Cette partie étant assez longue à coder nous avons préféré nous concentrer sur la partie front-end tout en gardant en tête que le projet serait récupéré l’année prochaine par un autre groupe d’étudiants qui auraient l’occasion de développer la partie.  </br>
                            La maquette réalisée s’éloigne quelque peu de la maquette mais conserve les principaux éléments présentés plus haut.
                            </p>
                            <figure><img src="images/app.png" alt="#"></figure>

                            <p>Les principales différences entre les maquettes et la première version de l’application se trouvent sur la dernière page qui présente le plaisir. Les icônes de météo ont été remplacées par des émoticônes afin de permettre à l’enfant de ne pas confondre les émotions du début d’évaluation avec le plaisir que la séance lui a procuré. </br>

                            D’autres onglets permettent de présenter les crédits, les informations et l’utilisation de l’application. </p>
