                            <p><strong>Nos grilles</strong> </p>
                            <p>Nous avons créé trois versions différentes des grilles d’observation. Chaque grille étant une évolution de la précédente. Afin d’identifier ce qui nous semblait à améliorer nous avons utilisé, pendant les séances en classe, les grilles de l’année passée et nous avons noté les éléments qui semblaient manquer lors de notre observation. </p>

                            <p>Le premier élément fut la courte échelle de notation sur le premier type de tableau. Nous avons donc augmenté cette échelle pour qu’elle court de 1 à 10, pour apporter plus de précisions dans la notation.</br>
                            Nous avons également décidé de supprimer entièrement le troisième type de tableau qui ne nous satisfaisait pas. En effet, il nous a été difficile de déterminer comment utiliser ce tableau : toutes les 5 minutes nous devions noter les comportements, mais de qui ? Un seul enfant, un petit groupe, la classe entière ? Ce manque de clarté nous a donc encouragés à l’enlever afin de le remplacer par un espace de texte dans lequel nous sommes libres de noter tous types de remarques quant à la séance (ce qui a été fait, les réactions liées au programme, les comportements, etc.). Ce choix implique cependant un temps d’analyse plus important qu’avec le tableau en fonction du temps mais nous avons préféré sacrifier ce temps afin d’être plus précis et plus libres quant à nos observations sur les séances. </br>
